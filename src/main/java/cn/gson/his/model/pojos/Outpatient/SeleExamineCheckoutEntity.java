package cn.gson.his.model.pojos.Outpatient;

public class SeleExamineCheckoutEntity {

    private int id;
    private String result;
    private String show;
    private String name;
    private String typtname;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getShow() {
        return show;
    }

    public void setShow(String show) {
        this.show = show;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTyptname() {
        return typtname;
    }

    public void setTyptname(String typtname) {
        this.typtname = typtname;
    }
}
